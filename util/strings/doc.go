// 4.Contains/ContainsAny/ContainsRune/ContainsSpace - does String contains only/none/any of these characters
// 5.EqualFold - compares two strings
// 6.Slice/Left/Right/Mid/Before/After/BeforeLast/AfterLast/Between/BetweenSame - substring extractions
// 7.Split/SplitAfter/SplitAfterN/SplitN - splits a String into an array of substrings
// 8.Remove/RemoveSpace/RemoveStart/RemoveEnd - removes part of a string
// 9.Repeat/Pad/PadStart/PadEnd/PadLeft/PadRight/Center - pads a string
// 10.ToLower/ToUpper/Title/ToTitle/Camel/UnCamel - changes the case of a string
// 11.Abbr/Anon/Anonymity/Anonymous - abbreviates a string using ellipsis or asterisk
